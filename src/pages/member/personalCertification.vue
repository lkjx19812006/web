        checkArr.push(checkNameDes);
        let checkImg = validation.checkArr(_self.formData.imgArr);
        checkArr.push(checkImg);
        for (let i = 0; i < checkArr.length; i++) {
          if (checkArr[i]) {
            this.$message({
              showClose: false,
              message: checkArr[i]
            });
            return;
          }
        }
